package com.java.Week3_InputFromUser;

public class IncrementDecrement {
    public static void main(String[] args) {

        int num = 0;
        num++;
        System.out.println(num);
        num++;
        System.out.println(num);
        num++;
        System.out.println(num);


        int num2 = 10;
        num2--;
        System.out.println( num2);
        num2--;
        System.out.println( num2);
        num2--;
        System.out.println( num2);



        int num3 = 0;
        ++num3;
        System.out.println(num3);


        int num4 = 10;
        --num4;
        System.out.println(num4);
    }
}
